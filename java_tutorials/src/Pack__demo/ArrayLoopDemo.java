package Pack__demo;

public class ArrayLoopDemo {

	public static void main(String[] args) {

		
		String[] classroom= {"a","b","c","d","e","f","g","h","i","j"};
		int count;
		
		for (count=0;count<10;count++) {
			System.out.println("상담 받은 학생  : "+classroom[count]);
			System.out.println("상담받은 학생 수 : "+(count+1));
			
		}
		
		
	}

}
